  function getByPath(obj, path) {
    return path.split(".").reduce(function(acc, key) {
      return acc && acc[key] != null ? acc[key] : null;
    }, obj);
  }

  function t(key) {
    return getByPath(translations[currentLanguage], key) || getByPath(translations.th, key) || key;
  }

  function applyTranslations(root) {
    root = root || document;
    root.querySelectorAll("[data-i18n]").forEach(function(node) {
      node.textContent = t(node.getAttribute("data-i18n"));
    });
    root.querySelectorAll("[data-i18n-placeholder]").forEach(function(node) {
      node.setAttribute("placeholder", t(node.getAttribute("data-i18n-placeholder")));
    });
    root.querySelectorAll("[data-i18n-title]").forEach(function(node) {
      node.setAttribute("title", t(node.getAttribute("data-i18n-title")));
    });
    document.documentElement.lang = currentLanguage === "th" ? "th" : "en";
    window.dispatchEvent(new CustomEvent("i18n:updated", { detail: { language: currentLanguage } }));
  }

  function renderSwitchers(root) {
    root = root || document;
    root.querySelectorAll("[data-language-switcher]").forEach(function(node) {
      node.innerHTML = ""
        + '<button type="button" class="site-nav-link' + (currentLanguage === "th" ? " is-primary" : "") + '" data-lang="th">' + t("common.languageThai") + "</button>"
        + '<button type="button" class="site-nav-link' + (currentLanguage === "en" ? " is-primary" : "") + '" data-lang="en">' + t("common.languageEnglish") + "</button>";
      node.querySelectorAll("[data-lang]").forEach(function(button) {
        button.addEventListener("click", function() {
          setLanguage(button.getAttribute("data-lang"));
        });
      });
    });
  }

  function setLanguage(language) {
    currentLanguage = language === "en" ? "en" : "th";
    localStorage.setItem(storageKey, currentLanguage);
    renderSwitchers(document);
    applyTranslations(document);
  }

  window.I18N = {
    t: t,
    getLanguage: function() { return currentLanguage; },
    setLanguage: setLanguage,
    applyTranslations: applyTranslations,
    renderSwitchers: renderSwitchers
  };

  document.addEventListener("DOMContentLoaded", function() {
    renderSwitchers(document);
    applyTranslations(document);
  });
